package LoopDemo;

public class WhileANDDoWhile {
	public static void main(String[] args) {
		
		
		int number=10;
		while(number<5)
		{
			System.out.println(number);
			number++;
		}
	              
		
		       //do while
		int x=10;
		do
		{
			System.out.println(x);
			x++;
		}while(x<5);


}
}
