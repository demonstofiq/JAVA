package operators;

public class Relational {
	public static void main(String[] args) {
		// (== , <== , >= , != )
		int x = 10;
		int y = 20;
		boolean res1 = x>=y;
		boolean res2 = x<=y;
		boolean res3 = x==y;
		boolean res4 = x!=y;
		
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);	
	}

}
